function setup() {
  cnv = createCanvas(800, 800); //(gridSize * cellSize + 10, gridSize * cellSize + 10);
  let cx = windowWidth / 2 - cnv.width / 2;
  let cy = windowHeight / 2 - cnv.width / 2;
  cnv.position(cx, cy);
  background(255);
  makeGrid();
  print(grid.length);
  showGrid();
  frameRate(1);
}
function draw() {
  background(255);
  makeGrid();
  showGrid();
}

function makeGrid() {
  for (let j = 0; j < 100; j++) {
    grid[j] = [];
    for (let i = 0; i < 100; i++) {
      mystr = random(lc);
      cstr = mystr.replaceAll(" ", "");
      grid[j][i] = new Lettr(i * sz, j * sz, cstr);

      //.replaceAll(" ", "")
    }
  }
}

function showGrid() {
  for (let j = 0; j < 100; j++) {
    for (let i = 0; i < 100; i++) {
      grid[j][i].show();
    }
  }
}
